using System.Globalization;

namespace YoutubeExplode.Models.ClosedCaptions
{
    /// <summary>
    /// Closed caption track metadata
    /// </summary>
    public class ClosedCaptionTrackInfo
    {
        /// <summary>
        /// Manifest URL
        /// </summary>
        public string Url { get; internal set; }

        /// <summary>
        /// Text culture
        /// </summary>
        public CultureInfo Culture { get; internal set; }

        /// <summary>
        /// Whether this caption track was generated automatically
        /// </summary>
        public bool IsAutoGenerated { get; internal set; }

        internal ClosedCaptionTrackInfo() { }
    }
}